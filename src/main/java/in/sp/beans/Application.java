package in.sp.beans;

public class Application {

	private String job_id;
	private String job_title;
	private String recruiter_mail;
	private String applicant_name;
	private String gender;
	private String high_edu;
	public String getJob_id() {
		return job_id;
	}
	public void setJob_id(String job_id) {
		this.job_id = job_id;
	}
	public String getJob_title() {
		return job_title;
	}
	public void setJob_title(String job_title) {
		this.job_title = job_title;
	}
	public String getRecruiter_mail() {
		return recruiter_mail;
	}
	public void setRecruiter_mail(String recruiter_mail) {
		this.recruiter_mail = recruiter_mail;
	}
	public String getApplicant_name() {
		return applicant_name;
	}
	public void setApplicant_name(String applicant_name) {
		this.applicant_name = applicant_name;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getHigh_edu() {
		return high_edu;
	}
	public void setHigh_edu(String high_edu) {
		this.high_edu = high_edu;
	}
}
